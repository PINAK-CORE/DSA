class Solution 
{
    public int strStr(String haystack, String needle)
    {
        // Q     : https://leetcode.com/problems/find-the-index-of-the-first-occurrence-in-a-string/
        // Sol   : https://leetcode.com/problems/find-the-index-of-the-first-occurrence-in-a-string/solutions/5323879/index-of-the-first-occurrence-in-a-string-eazy-understandable-simple-solution-java
        
        return haystack.indexOf(needle);
    }
}